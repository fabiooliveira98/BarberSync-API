const express = require('express');
const router = express.Router();
const AutenticacaoController = require('../controllers/autenticacao.controller');

/**
 * @swagger
 * /autenticacao/registrar:
 *   post:
 *     summary: Registra um novo usuário
 *     tags: [Autenticação]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - nome
 *               - email
 *               - senha
 *             properties:
 *               nome:
 *                 type: string
 *               email:
 *                 type: string
 *               senha:
 *                 type: string
 *               cargo:
 *                 type: string
 *                 enum: [cliente, admin]
 *               admin_key:
 *                 type: string
 *                 description: Chave secreta necessária apenas para o cargo admin
 *     responses:
 *       201:
 *         description: Usuário criado com sucesso
 *       400:
 *         description: Erro na requisição
 */
router.post('/registrar', AutenticacaoController.registrar);

/**
 * @swagger
 * /autenticacao/login:
 *   post:
 *     summary: Realiza o login do usuário
 *     tags: [Autenticação]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - senha
 *             properties:
 *               email:
 *                 type: string
 *               senha:
 *                 type: string
 *     responses:
 *       200:
 *         description: Login realizado com sucesso
 *       401:
 *         description: Credenciais inválidas
 */
router.post('/login', AutenticacaoController.login);

module.exports = router;
